
# Define a list of friends
friends = ['Alexander', 'Mia', 'Elijah', 'Noah', 'Emily', 'Charlotte', 'Mason']

# Print a message to announce the party
print("¡Haré una fiesta mañana por la noche!")

# Iterate through each friend in the list
for friend in friends:
    # Print a personalized message to invite the friend to the party
    print(friend + ', ¡Te invito a mi casa!')

# Print a message to express excitement about the party
print("¡Será divertido!")
