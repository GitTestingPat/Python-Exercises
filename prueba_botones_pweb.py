
# Define the button color
button_color = 'rosa'

# Check the button color and print the corresponding button type
if button_color == 'azul':
    # This is a save button
    print('Este es un botón de guardar')
elif button_color == 'amarillo':
    # This is a navigation button
    print('Este es un botón de navegación')
elif button_color == 'rojo':
    # This is a close button
    print('Este es un botón de cierre')
else:
    # This button color does not exist
    print('¡Este botón no existe!')
#
#In this code, we define the button color as 'rosa'. Then, we use an if-elif-else statement to check the button color and print the corresponding button type. If the button color is not 'azul', 'amarillo', or 'rojo', we print '¡Este botón no existe!'..</s>