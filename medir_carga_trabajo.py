

# Calculate the remaining time after completing tasks

# Total time available
total = 40

# Time required for each task
task_1 = 10.25 # 10 horas y 15 minutos se representan como 10.25 horas
task_2 = 5
task_3 = 2.5    # 2 horas y media se representan como 2.5 horas

# Calculate the remaining time after completing all tasks
result = total - task_1 - task_2 - task_3

# Print the remaining time
print(result)
