# Definición de la función print_tasks
def print_tasks(name, tasks_list):
    print(name, "se encarga de:", tasks_list)

# Listas de tareas para cada miembro del equipo
daniel_tasks_list = ["soplar las velas", "pedir un deseo"]
jennifer_tasks_list = ["dar un regalo", "cortar el pastel"]
gabriel_tasks_list = ["celebrar con todo el equipo", "hacer fotos"]

# Llamar a la función print_tasks para cada miembro del equipo
print_tasks("Daniel", daniel_tasks_list)
print_tasks("Jennifer", jennifer_tasks_list)
print_tasks("Gabriel", gabriel_tasks_list)
