
# Calculate the number
number = 10 // 3 * 2 + 7 % 3 - 5

# Print the number
print(number)

#
#In this code, we are calculating the number by performing the following operations:
#
#1. Integer division of 10 by 3: `10 // 3`
#2. Multiplication of the result from step 1 by 2: `(10 // 3) * 2`
#3. Modulo operation of 7 by 3: `7 % 3`
#4. Addition of the results from steps 2 and 3: `(10 // 3) * 2 + 7 % 3`