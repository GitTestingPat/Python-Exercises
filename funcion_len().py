
# Define a list of friends' names
friends = ['Alexander', 'Mia', 'Elijah', 'Noah', 'Emily', 'Charlotte', 'Mason']

# Iterate through each friend in the list
for friend in friends:
    # Print the friend's name and the number of letters in their name
    print('El nombre ' + friend + ' contiene ' + str(len(friend)) + ' letras.')

#
#In this code, we define a list called `friends` that contains the names of some friends. We then use a `for` loop to iterate through each friend in the list. Inside the loop, we print a sentence that includes the friend's name and the number of letters in their name. The `len()` function is used to calculate the number of letters in the friend'